print("1초마다 생산 데이터가 들어온다고 가정합니다.")
print("아래 데이터는 파이썬 문법 학습을 위한 교육용 가상 데이터입니다.\n")


for i in range(10):
    # --------------------------------------------------------
    # 교육용 가상 생산 데이터 생성
    # 실제 생산 설비에서 가져온 데이터가 아닙니다.
    # --------------------------------------------------------
    wafer_count = random.randint(1, 5)
    chips_per_wafer = random.randint(500, 800)

    total_chips = wafer_count * chips_per_wafer

    # 전체 칩 수의 최대 약 8% 범위 안에서 불량 칩 개수를 임의 생성합니다.
    # 이 값 역시 실제 불량률을 의미하지 않는 교육용 가상값입니다.
    defective_chips = random.randint(0, int(total_chips * 0.08))

    yield_rate = calculate_yield(
        wafer_count,
        chips_per_wafer,
        defective_chips
    )

    print("측정 번호:", i + 1)
    print("웨이퍼 개수:", wafer_count)
    print("웨이퍼 1장당 칩 개수:", chips_per_wafer)
    print("불량 칩 개수:", defective_chips)
    print("수율:", round(yield_rate, 2), "%")
    print("-" * 50)

    time.sleep(1)