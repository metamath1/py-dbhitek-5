# 수율 정의가 달라질 수 있습니다.
#
# 학습 목표:
# - 함수를 정의한다.
# - 매개변수를 사용한다.
# - 계산 결과를 return 한다.
# - 반복적으로 들어오는 데이터에 같은 함수를 재사용한다.
#
# 수강생은 calculate_yield() 함수 내부만 작성합니다.
# random과 time은 실시간 데이터처럼 보이게 하기 위한 제공 코드입니다.
