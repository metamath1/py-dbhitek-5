import random
import time


def calculate_yield(wafer_count, chips_per_wafer, defective_chips):
    # ========================================================
    # 수강생 작성 영역
    #
    # 목표:
    # 웨이퍼 개수, 웨이퍼 1장당 칩 개수, 불량 칩 개수를 사용해
    # 수율을 계산하고 return 하세요.
    #
    # 교육용 단순 계산식:
    # 전체 칩 수 = 웨이퍼 개수 * 웨이퍼 1장당 칩 개수
    # 정상 칩 수 = 전체 칩 수 - 불량 칩 수
    # 수율 = 정상 칩 수 / 전체 칩 수 * 100
    #
    # 주의:
    # 이 계산식은 파이썬 함수 학습을 위한 단순화된 수율 계산입니다.
    # 실제 제조 현장의 수율 계산은 제품/공정/검사 기준에 따라
    # 더 복잡할 수 있습니다.
    # ========================================================

    ###########################################################################
    # 여기에 코드 작성
    total_chips = wafer_count * chips_per_wafer
    good_chips = total_chips - defective_chips
    yield_rate = good_chips / total_chips * 100

