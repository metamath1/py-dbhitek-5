# ============================================================
# 웨이퍼 재고량 CRUD하는 프로그램
# ============================================================
# 이 예제는 실제 반도체 재고 시스템을 그대로 구현한 것이 아닙니다.
# 파이썬의 dict 자료형을 연습하기 위해
# "웨이퍼 ID별 재고 관리" 상황을 단순화한 교육용 예제입니다.
#
# 학습 목표:
# - dict 자료형의 key-value 구조를 이해한다.
# - 데이터를 추가(Create), 조회(Read), 수정(Update), 삭제(Delete)한다.
# - 웨이퍼 ID처럼 고유한 식별자를 key로 사용하는 방식을 익힌다.
#
# 수강생은 "수강생 작성 영역"만 작성합니다.
# 메뉴 출력, 입력 처리, 반복 실행 코드는 과제 진행을 위한 제공 코드입니다.
# ============================================================

inventory = {
    "WAFER-001": {"quantity": 25, "status": "입고완료"},
    "WAFER-002": {"quantity": 40, "status": "검사대기"},
    "WAFER-003": {"quantity": 15, "status": "생산투입가능"}
}


def show_all_inventory():
    # 전체 재고를 보기 좋게 출력하기 위한 제공 함수입니다.
    # 함수 문법을 아직 자세히 배우지 않았더라도 이 부분은 수정하지 않습니다.
    print("\n[전체 웨이퍼 재고]")

    if len(inventory) == 0:
        print("등록된 웨이퍼가 없습니다.")
        return

    for wafer_id, info in inventory.items():
        print(
            wafer_id,
            "| 수량:", info["quantity"],
            "| 상태:", info["status"]
        )


while True:
    print("\n==============================")
    print("웨이퍼 재고 관리 프로그램")
    print("==============================")
    print("1. 전체 재고 조회")
    print("2. 신규 웨이퍼 등록")
    print("3. 특정 웨이퍼 조회")
    print("4. 웨이퍼 정보 수정")
    print("5. 웨이퍼 삭제")
    print("0. 종료")

    menu = input("메뉴를 선택하세요: ")

    if menu == "1":
        show_all_inventory()

    elif menu == "2":
        wafer_id = input("등록할 웨이퍼 ID를 입력하세요: ")
        quantity = int(input("수량을 입력하세요: "))
        status = input("상태를 입력하세요: ")

        # ====================================================
        # 수강생 작성 영역: Create
        #
        # 목표:
        # 입력받은 wafer_id, quantity, status를 사용하여
        # inventory에 새로운 웨이퍼 정보를 등록하세요.
        # ====================================================

        ##################################################################
        # 여기에 코드 작성
        # Create: 신규 웨이퍼 등록 
        inventory[wafer_id] = {"quantity": quantity, "status": status}
        ###########################################################################

        print("등록 완료")

    elif menu == "3":
        wafer_id = input("조회할 웨이퍼 ID를 입력하세요: ")

        # ====================================================
        # 수강생 작성 영역: Read
        #
        # 목표:
        # 입력받은 wafer_id가 inventory에 존재하면
        # 해당 웨이퍼 정보를 출력하세요.
        #
        # 존재하지 않으면
        # "해당 웨이퍼 ID가 없습니다."를 출력하세요.
        # ====================================================

        ###########################################################################
        # 여기에 코드 작성
        # Read: 특정 웨이퍼 조회


        ###########################################################################

    elif menu == "4":
        wafer_id = input("수정할 웨이퍼 ID를 입력하세요: ")
        quantity = int(input("수정할 수량을 입력하세요: "))
        status = input("수정할 상태를 입력하세요: ")

        # ====================================================
        # 수강생 작성 영역: Update
        #
        # 목표:
        # wafer_id가 존재하면 해당 웨이퍼의 quantity와 status를
        # 입력받은 값으로 수정하세요.
        # ====================================================

        ###########################################################################
        # 여기에 코드 작성
        # Update: 웨이퍼 정보 수정
        # 사용자로 부터 입력받은 wafer_id가 inventory에 있다면  
        # inventory[wafer_id]['quantity'] = quantity
        # inventory[wafer_id]['status'] = status
        # 없다면 아이디가 없다고 사용자에게 알려줄 것
        if wafer_id in inventory:
            inventory[wafer_id]["quantity"] = quantity
            inventory[wafer_id]["status"] = status
            print("수정 완료")
        else:
            print("해당 웨이퍼 ID가 없습니다.")
        ###########################################################################

    elif menu == "5":
        wafer_id = input("삭제할 웨이퍼 ID를 입력하세요: ")

        # ====================================================
        # 수강생 작성 영역: Delete
        #
        # 목표:
        # wafer_id가 존재하면 inventory에서 해당 데이터를 삭제하세요.
        # ====================================================

        ###########################################################################
        # 여기에 코드 작성
        # Delete: 웨이퍼 정보 삭제


        ###########################################################################

    elif menu == "0":
        print("프로그램을 종료합니다.")
        break

    else:
        print("잘못된 메뉴입니다. 다시 선택하세요.")